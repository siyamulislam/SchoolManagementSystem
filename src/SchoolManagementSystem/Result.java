    private void deplace(int i) {
        try {
            txtid.setText(model.getValueAt(i, 0).toString());
            txtname.setText(model.getValueAt(i, 1).toString());
            txtclass.setText(model.getValueAt(i, 2).toString());
            if (model.getValueAt(i, 3).toString().equals("Male")) {
                rbmale.setSelected(true);
            } else if (model.getValueAt(i, 3).toString().equals("Female")) {
                rbfemale.setSelected(true);
            }
            txtdob.setText(model.getValueAt(i, 4).toString());
            txtphone.setText(model.getValueAt(i, 5).toString());
            txtaddress.setText(model.getValueAt(i, 6).toString());

        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "displacement error!" + e.getLocalizedMessage());
        }
    }

